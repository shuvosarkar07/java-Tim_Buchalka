public class TraditionalSwitch {
    public static void main(String[] args) {
        int age = 21;

        switch (age) {
            case 20:
                System.out.println("Your age is 20");
                break;
            case 21:
                System.out.println("Your age is 21");
                break;
            case 22:
                System.out.println("Your age is 22");
                break;
            case 23:
                System.out.println("Your age is 23");
                break;
            case 24:
                System.out.println("Your age is 24");
                break;
        }

    }
}
